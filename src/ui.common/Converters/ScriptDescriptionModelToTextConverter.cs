using System;
using System.Diagnostics.CodeAnalysis;
using System.Windows.Data;
using Apollo.UI.Wpf.Views.Scripting;

namespace Apollo.UI.Wpf.Converters
{
    /// <summary>
    /// Converts a <see cref="ScriptDescriptionModel"/> to a text description of the model.
    /// </summary>
    [ExcludeFromCodeCoverage]
    public sealed class ScriptDescriptionModelToTextConverter : IValueConverter
    {
        /// <summary>
        /// Converts a value.
        /// </summary>
        /// <param name="value">The value produced by the binding source.</param>
        /// <param name="targetType">The type of the binding target property.</param>
        /// <param name="parameter">The converter parameter to use.</param>
        /// <param name="culture">The culture to use in the converter.</param>
        /// <returns>A converted value. If the method returns null, the valid null value is used.</returns>
        public object Convert(object value, Type targetType, object parameter, System.Globalization.CultureInfo culture)
        {
            var model = value as ScriptDescriptionModel;
            return (model != null) ? model.Description : string.Empty;
        }

        /// <summary>
        /// Converts a value.
        /// </summary>
        /// <param name="value">The value produced by the binding source.</param>
        /// <param name="targetType">The type of the binding target property.</param>
        /// <param name="parameter">The converter parameter to use.</param>
        /// <param name="culture">The culture to use in the converter.</param>
        /// <returns>A converted value. If the method returns null, the valid null value is used.</returns>
        public object ConvertBack(object value, Type targetType, object parameter, System.Globalization.CultureInfo culture)
        {
            throw new NotImplementedException();
        }
    }
}
